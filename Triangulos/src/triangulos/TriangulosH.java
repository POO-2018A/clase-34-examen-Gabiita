package triangulos;

/**
 *
 * @author ESFOT
 */
public class TriangulosH {
    public double ladoA;
    public double ladoB;
   
    

    public TriangulosH(double ladoA, double ladoB) {
        this.ladoA = ladoA;
        this.ladoB = ladoB;
        
    }
    
   
    public double getLadoA() {
        return ladoA;
    }

    public void setLadoA(double ladoA) {
        this.ladoA = ladoA;
    }

    public double getLadoB() {
        return ladoB;
    }

    public void setLadoB(double ladoB) {
        this.ladoB = ladoB;
    }

    

 
}
